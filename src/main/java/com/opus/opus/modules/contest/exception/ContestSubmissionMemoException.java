package com.opus.opus.modules.contest.exception;

import com.opus.opus.global.base.BaseException;
import com.opus.opus.global.base.BaseExceptionType;

public class ContestSubmissionMemoException extends BaseException {

    private final ContestSubmissionMemoExceptionType exceptionType;

    public ContestSubmissionMemoException(final ContestSubmissionMemoExceptionType exceptionType) {
        super(exceptionType.errorMessage());
        this.exceptionType = exceptionType;
    }

    @Override
    public BaseExceptionType exceptionType() {
        return exceptionType;
    }
}
